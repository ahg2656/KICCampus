package pack.model;

public class JikwonDto {
	private String jikwon_name, buser_name, jikwon_jik, buser_tel, jikwon_gen;

	public String getJikwon_name() {
		return jikwon_name;
	}

	public void setJikwon_name(String jikwon_name) {
		this.jikwon_name = jikwon_name;
	}

	public String getBuser_name() {
		return buser_name;
	}

	public void setBuser_name(String buser_name) {
		this.buser_name = buser_name;
	}

	public String getJikwon_jik() {
		return jikwon_jik;
	}

	public void setJikwon_jik(String jikwon_jik) {
		this.jikwon_jik = jikwon_jik;
	}

	public String getBuser_tel() {
		return buser_tel;
	}

	public void setBuser_tel(String buser_tel) {
		this.buser_tel = buser_tel;
	}

	public String getJikwon_gen() {
		return jikwon_gen;
	}

	public void setJikwon_gen(String jikwon_gen) {
		this.jikwon_gen = jikwon_gen;
	}
}
